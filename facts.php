<h1> Facts about donation blood </h1>
<ul>
<li>Donating blood is a safe process. A sterile needle is used only once for each donor and then discarded.</li>
<li>Blood donation is a simple four-step process: registration, medical history&nbsp;and&nbsp;mini-physical, donation and refreshments.</li>
<li>Every blood donor is given a mini-physical, checking the donor&#39;s temperature, blood pressure, pulse and&nbsp;hemoglobin to ensure it is safe for the donor to give blood.</li>
<li>The actual blood donation typically takes less than 10-12 minutes. The entire process, from the time you arrive to the time you leave, takes about an hour and 15 min.</li>
<li>The average adult has about 10 pints of blood in his body. Roughly 1 pint is given during a donation.</li>
<li>A healthy donor may donate red blood cells every 56 days, or double red cells every 112 days.</li>
<li>A healthy donor may donate platelets as few as&nbsp;7 days apart, but a maximum of 24 times a year.</li>
<li>All donated blood is tested for HIV, hepatitis B and C, syphilis and other infectious diseases before it can be released to hospitals.</li>
<li>Information you give to the American Red Cross during the donation process is confidential. It may not be released without your permission except as directed by law.</li>
